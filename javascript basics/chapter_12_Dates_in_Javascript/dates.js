let myDate = new Date();
console.log(myDate);
console.log(myDate.getTime()); // provides time in seconds 
console.log(myDate.getFullYear()); // Provides the year
console.log(myDate.getDay()); // Provides the index of the day
console.log(myDate.getMinutes()); // provides the minutes of the time
console.log(myDate.getHours()); // provides the hours of the time
console.log(myDate.getSeconds()); // provides the seconds of the time